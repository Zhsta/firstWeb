package com.zhao.util;

public class StudentPropertyUtil {
    private String username;
    private String basicInfo;
    private String schoolrool;
    private String rewardAndPunish;
    private String minor;
    private String schoolRoolChange;
    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getBasicInfo() {
        return basicInfo;
    }

    public void setBasicInfo(String basicInfo) {
        this.basicInfo = basicInfo;
    }

    public String getSchoolrool() {
        return schoolrool;
    }

    public void setSchoolrool(String schoolrool) {
        this.schoolrool = schoolrool;
    }

    public String getRewardAndPunish() {
        return rewardAndPunish;
    }

    public void setRewardAndPunish(String rewardAndPunish) {
        this.rewardAndPunish = rewardAndPunish;
    }

    public String getMinor() {
        return minor;
    }

    public void setMinor(String minor) {
        this.minor = minor;
    }

    public String getSchoolRoolChange() {
        return schoolRoolChange;
    }

    public void setSchoolRoolChange(String schoolRoolChange) {
        this.schoolRoolChange = schoolRoolChange;
    }


}
