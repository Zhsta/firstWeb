package com.zhao.util;

public class TeacherPropertyUtil {
    private String username;
    private String basicInfo;
    private String science;
    private String career;
    private String rewardAndPunish;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getBasicInfo() {
        return basicInfo;
    }

    public void setBasicInfo(String basicInfo) {
        this.basicInfo = basicInfo;
    }

    public String getScience() {
        return science;
    }

    public void setScience(String science) {
        this.science = science;
    }

    public String getCareer() {
        return career;
    }

    public void setCareer(String career) {
        this.career = career;
    }

    public String getRewardAndPunish() {
        return rewardAndPunish;
    }

    public void setRewardAndPunish(String rewardAndPunish) {
        this.rewardAndPunish = rewardAndPunish;
    }
}
